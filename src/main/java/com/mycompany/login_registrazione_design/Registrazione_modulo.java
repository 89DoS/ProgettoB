    public boolean verificaCampi(){
        String fnome = jTextFieldNome.getText();
        String fcognome = jTextFieldCognome.getText();
        String email = jTextFieldEmail.getText();
        String username = jTextFieldUserName.getText();
        String passwd = String.valueOf(jPasswordField1.getPassword()); 
        String passwdConf = String.valueOf(jPasswordField2.getPassword());
        
        if(fnome.trim().equals("") || fcognome.trim().equals("") || email.trim().equals("") || username.trim().equals("") || passwd.trim().equals("") || passwdConf.trim().equals("") ){
            JOptionPane.showMessageDialog(null, "uno o più campi sono vuoti");
            return false;
        }
        // ora controllo se le password coincidono
        else if(!passwd.equals(passwdConf)){
          JOptionPane.showMessageDialog(null, "la password non coincide");  
          return false;
        }
        
        //se invece è tutto ok..
        else{
            return true;
        }
           
    }
    
    // creo un afunzione per verificare se username e email esistono già nel database
    public boolean checkExist(String username, String email){
        
        PreparedStatement st;
        ResultSet rs;
        PreparedStatement st1;
        ResultSet rs1;
        
        
        boolean account_exist = false;
       
        
        String query ="SELECT * FROM playerip WHERE username = ? ";
        String query1 ="SELECT * FROM playerip WHERE email = ?";
        
        
        try {
            st = Connessione_db.getConnection().prepareStatement(query);
            st1 = Connessione_db.getConnection().prepareStatement(query1);
            st.setString(1, username);
            st1.setString(1, email);
            rs = st.executeQuery();
            rs1 = st1.executeQuery();
            if(rs.next()){
                account_exist = true; 
                JOptionPane.showMessageDialog(null, "username già esistente", "Username Failed", 2);       
            }
            if(rs1.next()){
                account_exist = true; 
                JOptionPane.showMessageDialog(null, "email già esistente", "email Failed", 2);       
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Registrazione_modulo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return account_exist;
        
    }
    
    
    
