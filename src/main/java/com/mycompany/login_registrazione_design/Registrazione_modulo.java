    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        String fnome = jTextFieldNome.getText();
        String fcognome = jTextFieldCognome.getText();
        String email = jTextFieldEmail.getText();
        String username = jTextFieldUserName.getText();
        String passwd = String.valueOf(jPasswordField1.getPassword()); 
        String passwdConf = String.valueOf(jPasswordField2.getPassword()); 
        
        Random rand = new Random();
        int codice =rand.nextInt(10000);
        Random randpass = new Random();
        int codicepass =randpass.nextInt(10000);
        
        
        
        
        
        
        if (verificaCampi()){
            if (!checkExist(username, email)){
            
               PreparedStatement ps;
               ResultSet rs;
               PreparedStatement st1;              
               String registerUserQuery = "INSERT INTO playerip (nome, cognome, email, password, username, codice) VALUES (?,?,?,?,?,?)";
               String query1 ="UPDATE playerip SET codicepass = ?";
            try {
                
                ps = Connessione_db.getConnection().prepareStatement(registerUserQuery);
                ps.setString(1, fnome);
                ps.setString(2, fcognome);
                ps.setString(3, email);
                ps.setString(4, passwd);
                ps.setString(5, username);              
                ps.setInt(6, codice);
                
           
                
               
            
                
                
                if(ps.executeUpdate() != 0 ){
                    SendingEmail se = new SendingEmail(email, codice);
                    se.sendMail();
                    JOptionPane.showMessageDialog(null, "il tuo account è stato creato");
                    st1 = Connessione_db.getConnection().prepareStatement(query1);
                    st1.setInt(1, codicepass);
                    st1.executeUpdate();
                }
                else {
                    JOptionPane.showMessageDialog(null, "Errore: controlla le tue informazioni");
                    
                }
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Registrazione_modulo.class.getName()).log(Level.SEVERE, null, ex);
            }
               
               
            
        }
        
        }
    }//GEN-LAST:event_jButton_RegisterActionPerformed

